from turtle import *
screen = Screen()
screen.setup(1.0,1.0)
fillcolor("yellow")
pencolor("yellow")
begin_fill()
circle(120)
end_fill()
pencolor("black")
pensize(10)
penup()
goto(-50,120)
pendown()
fillcolor("white")
begin_fill()
circle(30)
end_fill()
penup()
forward(100)
pendown()
begin_fill()
circle(30)
end_fill()
fillcolor("red")
penup()
backward(100)
right(90)
forward(50)
pendown()
begin_fill()
circle(50,180)
left(90)
forward(100)
end_fill()
hideturtle()
done()